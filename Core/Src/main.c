/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init();
  MX_TIM1_Init();
  MX_USART3_UART_Init();
  /* USER CODE BEGIN 2 */

  // Point timer slave mode controller to TI1FP1
  TIM1->SMCR &= ~TIM_SMCR_TS; // Clear old trigger source selection
  TIM1->SMCR |= (5 << TIM_SMCR_TS_Pos);

  // Set slave mode to trigger mode
  TIM1->SMCR &= ~TIM_SMCR_SMS; // Clear old slave mode
  TIM1->SMCR |= (6 << TIM_SMCR_SMS_Pos);

  // Arm DMA for ADC conversions
  HAL_ADC_Start_DMA(&hadc1, (uint32_t*)sample_buffer, SAMPLE_BUFFER_SIZE);

  // Arm timer 1 channel 1
  HAL_TIM_IC_Start_IT(&htim1, TIM_CHANNEL_1);

  // Arm timer 1 channel 2
  HAL_TIM_OC_Start_IT(&htim1, TIM_CHANNEL_2);

  // Manually enable update interrupt
  __HAL_TIM_ENABLE_IT(&htim1, TIM_IT_UPDATE);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  if(full == 1){ // can safely process samples

		  //start = HAL_GetTick();

		  // calculate DC offset
		  uint32_t sum = 0;

		  for (int i = 0; i < SAMPLE_BUFFER_SIZE; i++){
			  sum += sample_buffer[i];
		  }

		  uint16_t offset = sum / SAMPLE_BUFFER_SIZE;

		  for (int i = 0; i < SAMPLE_BUFFER_SIZE; i++){ // remove DC offset from each sample

			  centered_samples[i] = (float)(int32_t)sample_buffer[i] - offset; // cast for signed arithmetic
		  }

		  // handle initial sample
		  int16_t previous = centered_samples[0];
		  uint32_t energy = (uint32_t)(previous * previous);  // total energy
		  uint32_t peak = abs(previous); // peak amplitude
		  uint16_t inversions = 0; // used to calculate zero-crossing rate

		  for(int i = 1; i < SAMPLE_BUFFER_SIZE; i++){ // frame features calculation

			  int32_t current = centered_samples[i];

			  // update energy
			  energy += (uint32_t)(current * current);

			  // update inversions
			  if((previous > 0 && current < 0) || (previous < 0 && current > 0)){ // a sign change occurs
				  inversions++;
			  }

			  // update peak amplitude
			  if(abs(current) > peak){
				  peak = abs(current);
			  }

			  previous = current;
		  }

		  float zcr = (float)inversions / SAMPLE_BUFFER_SIZE;

		  // process fft (convert amplitude to frequency)
		  arm_rfft_fast_instance_f32 fft;
		  arm_rfft_fast_init_f32(&fft, SAMPLE_BUFFER_SIZE); // initialize instance

		  arm_rfft_fast_f32(&fft, centered_samples, centered_samples, 0); // do fft

		  float magnitude_arr[SAMPLE_BUFFER_SIZE / 2];

		  arm_cmplx_mag_f32(centered_samples, magnitude_arr, SAMPLE_BUFFER_SIZE / 2); // calculate frequency magnitudes

		  float max_magnitude;
		  uint32_t max_bin;

		  arm_max_f32(magnitude_arr, SAMPLE_BUFFER_SIZE / 2, &max_magnitude, &max_bin); // get bin with greatest magnitude

		  float dominant_frequency = (max_bin * SAMPLING_RATE) / SAMPLE_BUFFER_SIZE;

		  // calculate number of chars needed
		  int chars = snprintf(NULL, 0, "%lu,%f,%lu,%f\r\n", energy, zcr, peak, dominant_frequency);

		  char feature_string[chars + 1];

		  // pack features into string
		  snprintf(feature_string, sizeof(feature_string), "%lu,%f,%lu,%f\r\n", energy, zcr, peak, dominant_frequency);

		  //end = HAL_GetTick();

		  //total = end - start;

		  HAL_UART_Transmit_DMA(&huart3, (uint8_t*)feature_string, chars); // transmit feature data

		  full = 0;

		  HAL_UART_Transmit_DMA(&huart2, (uint8_t*)P_END_MESSAGE, P_END_MESSAGE_LENGTH); // send processing end notification
	  }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

